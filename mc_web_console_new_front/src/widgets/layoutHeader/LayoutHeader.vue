<script setup lang="ts">
import TopBarWorkspaces from './ui/topBarWorkspaces/TopBarWorkspaces.vue';
import TopBarToolset from './ui/topBarToolset/TopBarToolset.vue';
import { reactive } from 'vue';

const state = reactive({
  openedMenu: '',
});

const hideMenu = () => {
  state.openedMenu = '';
};

const handleOpenMenu = (menuId: any) => {
  if (state.openedMenu === menuId) {
    hideMenu();
  } else {
    state.openedMenu = menuId;
  }
};
</script>

<template>
  <div class="top-bar">
    <!-- TODO: 1. topbar workspaces -->
    <top-bar-workspaces ref="topBarWorkspaceRef" class="top-bar-workspace" />
    <!-- TODO: 2. topbar toolset -->
    <top-bar-toolset
      ref="topBarToolsetRef"
      class="toolset"
      :opened-menu="state.openedMenu"
      @open-menu="handleOpenMenu"
      @hide-menu="hideMenu"
    />
  </div>
</template>

